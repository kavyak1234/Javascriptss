// / ARITHAMETIC OPERATORS

let number1 = 77;
let number2 = 67;

// ADDITION

let sum = number1 + number2;

console.log(sum);

// SUBTRACTION

let difference = number1 - number2;

console.log(difference);

// MULTIPLICATION

let product = number1 * number2;

console.log(product);

// DIVISION

let quotient = number1 / number2;

console.log(quotient);

// MODULUS

let reminder = number1 % number2;

console.log(reminder);

// increment operator

let num3 = 10;
num3++;
console.log(num3, "after increment");

let num4 = 3;

num4--;
console.log(num4, "after decrement");

num4--

console.log(num4, "after decrement");
